/*
Aritméticos - + - / * ** %
+ - Adição/concatenação;
- - Subtração;
/ - Divisão;
* - Multiplicação;
** - Potenciação;
% - Resto da divisão.
++ - Incremento;
-- - Decremento;
atribuição - += -= /= *= %= **=;
NaN - Not a Number

*/

const num1 = 10;
const num2 = 5;

console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 / num2);
console.log(num1 * num2);
console.log(num1 ** num2);
console.log(num1 % num2);

/*
Ordem de precedência:
1 - () parenteses;
2- ** potenciação;
3 -  * / % multiplicação, divisão e resto da divisão (o que vier primeiro lendo da esquerda para a direita);
4 - + -.
*/